<?php

class Cangaroo extends Animal
{
    private string $jumphigh;

    public function __construct(string $name,int $legs,string $jumphigh)
    {
        $this->name;
        $this->legs;
        $this->jumphigh;
    }


public function getname() {
    return $this->name;
}
public function getlegs() {
    return $this->legs;   
}
public function getjumphigh() {
    return $this->jumphigh;
}
}